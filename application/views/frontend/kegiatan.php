              <label>Lokakarya IAPS 4.0</label>
              <p class="text-justify">
                Lokakarya ini diselenggarakan di Kampus IT Del, Laguboti pada tanggal 18-19 Maret 2019 dengan mengundang Bapak Dr. Ir. I Gde Nyoman Merthayasa, M.Eng sebagai pemateri. Pada hari pertama lokakarya berjalan 2 (dua) sesi. Sedangkan untuk hari kedua, lokakarya hanya berjalan 1 (satu) sesi, lalu dikuti dengan penyusunan Laporan Evaluasi Diri (LED) secara mandiri oleh masing-masing peserta. Topik yang diangkat pada lokakarya ini adalah Kebijakan Nasional Sistem Penjaminan Mutu Pendidikan Tinggi – SPMI dan SPME, Standar Nasional Pendidikan Tinggi dan Audit Mutu Internal (AMI), Pedoman Penyusunan Laporan Kinerja Program Studi (LKPS) dan Pedoman Penyusunan Laporan Evaluasi Diri (LED). Selain penyajian materi, di lokakarya ini juga ada kegiatan penyusunan Laporan Evaluasi Diri (LED) oleh masing-masing prodi yang nantinya akan diberikan saran perbaikan oleh pemateri. Lokakarya ini memang dikhususkan untuk persiapan program studi yang akan melakukan akreditasi dengan borang versi terbaru (IAPS 4.0).  
