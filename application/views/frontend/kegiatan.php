                <label>Workshop Peningkatan Akreditasi Program Studi Kopertis Wilayah I</label>
                <p class="text-justify">
                  Workshop ini diselenggarakan pada tanggal 23 -25 April 2018 di Hotel Internasional Sibayak, Berastagi. Pembicara yang memberikan materi pada workshop tersebut ada sebanyak 4 orang  yakni tim asesor dan yang pernah menjadi tim asesor dan validator. Pemateri yang diundang ialah Dr. Abdurahman Saputra; Dr.Ir.Darwin., M.Pd; Dr.Sanusi Hasibuan, M.Kes; dan Prof.Dr.Ir.Ahmad Rafiqi Tantawi, MS. Peserta workshop adalah perwakilan dari 50 PTS di wilayah I Region luar Medan. Adapun materi pada workshop ini adalah : Pengelolaan Program Studi Berbasis Akreditasi, Strategi penyusunan Instrumen Akreditasi Prodi Standar 1 s.d 7, dan Pengelolaan Perguruan Tinggi Swasta (PTS) Sehat. Workshop ini sangat bermanfaat bagi IT Del dalam upaya peningkatan akreditasi program studi dan menjadi wadah untuk menjalin komunikasi yang baik antar sesama PTS terkait akreditasi di program studi masing-masing.
                </p>
