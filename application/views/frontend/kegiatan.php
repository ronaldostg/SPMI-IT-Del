                </p>
              </li>
              <li>
                <label>Lokakarya Sistem Penjaminan Mutu Internal (SPMI)</label>
                <p class="text-justify">
                  Lokakarya ini dilakukan di Kampus Institut Teknologi Del Kabupaten Toba Samosir pada tanggal 17-19 Juli 2018. Kegiatan ini merupakan bagian dari skema Program Asuh Perguruan Tinggi Unggul Tahun 2018 yang diberikan oleh Universitas Kristen Petra Surabaya. Lokakarya ini sangat bermanfaat karena setiap perguruan tinggi mempunyai kewajiban melaksanakan penjaminan mutu perguruan tingginya sesuai dengan standar-standar yang telah ditetapkan. Fasilitator lokakarya ini yaitu Dr. Dra. Jenny Mochtar, M.A. dan Ir. Emmy Hosea, M.Eng.Sc. Materi yang disampaikan untuk lokakarya yaitu Sistem Penjaminan Mutu Internal Perguruan Tinggi, dokumen mutu, dan audit mutu.
                </p>
              </li>
