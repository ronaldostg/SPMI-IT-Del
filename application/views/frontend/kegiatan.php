                </label>
                <p class="text-justify">
                  Kegiatan workshop dilaksanakan di Hotel Le Polonia Medan pada tanggal 17 November 2018.  Acara berlangsung kurang lebih 9 (Sembilan) jam. Pemateri pada workshop ini adalah Prof. Drs. T. Basaruddin, M.Sc., Ph.D (Direktur Dewan Eksekutif BAN-PT) dan  Surono dengan topik Penyusunan Instrumen Akreditasi Perguruan Tinggi (AIPT 3.0) dan Peranan BNSP Dalam Lembaga Sertifikasi Profesi. 
                  Kegiatan ini sangat bermanfaat terutama untuk perguruan tinggi yang sedang fokus pada perbaikan sistem penjaminan mutunya, sehingga besar kemungkinan untuk mendapatkan akreditasi baik ( A atau B) perguruan tinggi maupun program studi juga sertifikasi yang terpercaya profesi dosen tetap suatu perguruan tinggi.

